/**
 * Get all categories
 */
export function getAllCategories(): AssistantCategory[] {
  return Object.values(AssistantCategory);
}

/**
 * Get all tags from preset assistants
 */
export function getAllTags(): string[] {
  const tagsSet = new Set<string>();
  PRESET_ASSISTANTS.forEach(assistant => {
    assistant.tags?.forEach(tag => tagsSet.add(tag));
  });
  return Array.from(tagsSet).sort();
}

/**
 * Get assistants by category
 */
export function getAssistantsByCategory(category: AssistantCategory): PresetAssistant[] {
  return PRESET_ASSISTANTS.filter(assistant => 
    assistant.category.includes(category)
  );
}

/**
 * Get assistant by ID
 */
export function getAssistantById(id: string): PresetAssistant | undefined {
  return PRESET_ASSISTANTS.find(assistant => assistant.id === id);
}

/**
 * Search assistants by query
 */
export function searchAssistants(query: string): PresetAssistant[] {
  const lowerQuery = query.toLowerCase();
  return PRESET_ASSISTANTS.filter(assistant =>
    assistant.title.toLowerCase().includes(lowerQuery) ||
    assistant.desc.toLowerCase().includes(lowerQuery) ||
    assistant.tags?.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

/**
 * Get category label
 */
export function getCategoryLabel(category: AssistantCategory, lang: 'zh' | 'en' = 'zh'): string {
  return CATEGORY_LABELS[category][lang];
}
